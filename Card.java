public class Card{
	
	// Attributes
	private int cardIndex;
	private int cardValue;
	
	// Methods
	public int getValueOf(){
		return cardValue;
	}
	
	public int getIndexOf(){
		return cardIndex;
	}
	
	// Constructor
	public Card(int cardValue){
		this.cardIndex = cardIndex;
		this.cardValue = cardValue;
	}
}